import Head from 'next/head';

// Sanity Config
import { sanityClient } from 'lib/sanity.server';
import { howItWorksPageQuery } from 'lib/sanity.queries';

// sections
import Hero from 'modules/HowItWorks/Hero';
import BackOnTrack from 'modules/HowItWorks/BackOnTrack';
import Medications from 'modules/HowItWorks/Medications';
import Faq from 'modules/HowItWorks/Faq';
import Membership from 'modules/HowItWorks/Membership';

export default function HowItWorks({ pageData }) {
  const sectionsData = pageData?.sections;

  return (
    <>
      <Head>
        <title>How it works</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Hero data={sectionsData.hero} />

      <BackOnTrack data={sectionsData.howItWorks} />

      <Membership data={sectionsData.membership} />

      <Medications data={sectionsData.medications} />

      <Faq data={sectionsData.faqs} />
    </>
  );
}

export const getStaticProps = async () => {
  const pageData = await sanityClient.fetch(howItWorksPageQuery);

  return {
    props: { pageData },
  };
};
